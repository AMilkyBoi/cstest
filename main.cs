/* Tells the program to use the System namespace */
using System;

/* The namespace declaration indicates a collection of classes */
namespace HelloWorld
{
    /* Classes can contain multiple methods, which define the behavior of the class */
    class Program
    {
        /* Defines the 'Main' method, which states what the class does when it is executed */
        static void Main(string[] args)
        {
            /* Console.WriteLine writes a line to the console, which can then be read later */
            Console.WriteLine("Hello World");
        }
    } 
}
